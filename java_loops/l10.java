package java_loops;

import java.util.Scanner;

public class l10 {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        String a="a b c d e f g h i j k l m n o p q r s t u v w x y z";
        for(int i=1;i<=n;i++){
            System.out.println(a);
        }
        sc.close();
    }
    
}
